def _get_zipregion_group_names(cls, cr, uid, context=None):
    obj = cls.pool.get('res.zip_region_group')
    ids = obj.search(cr, uid, [], context=context)
    res = obj.read(cr, uid, ids, ['id', 'name'], context)
    return [(r['id'], r['name']) for r in res]
